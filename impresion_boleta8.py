#INPUT
pi=float(input("Ingrese el numero de pi: "))
radio1=float(input("Ingrese el numero  de radio 1: "))
radio2=float(input("Ingrese numero de radio 2: "))

#PROCESSING
area=(pi+radio1*radio2)

#OUTPUT
print ("################################")
print ("# CALCULAR EL AREA DEL CIRCULO #")
print ("# PI:", pi)
print ("# RADIO 1:", radio1)
print ("# RADIO 2:", radio2)
print ("# EL AREA DEL CIRCULO ES:", area)
print ("#################################")
